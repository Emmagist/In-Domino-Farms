  //Contact us
  $('.contact-form').submit(function (e) {
    e.preventDefault();
    const formData = new FormData(this);

    $.ajax({
        url: 'libs/fetchAjax.php?pg=205',
        method: 'POST',
        dataType: 'json',
        data: formData,
        contentType: false,
        processData: false,
        // cache: false,
        success: (param) => { 
            if (param.success) {
                
                $('.productSuccess').fadeIn()
                $('.productSuccess').text(param.success);
                setInterval(() => {
                    $('.productSuccess').fadeOut();
                    location.reload();
                }, 6000);
            }else if(param.error){
                $('.productError').fadeIn()
                $('.productError').text(param.error);
                setInterval(() => {
                    $('.productError').fadeOut();
                }, 6000);
            }
        },
    });
    return false;
  });

  
 </script>